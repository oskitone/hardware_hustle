import { isUndefined } from "lodash";
import { Open_Sans } from "next/font/google";
import { useRouter } from "next/router";
import Head from "next/head";

import { makeTurnData } from "components/turn";
import Page from "components/page";
import RollGrid from "components/roll-grid";
import Rules from "components/rules.mdx";
import Sheet from "components/sheet";
import Sidebar from "components/sidebar";
import TurnGrid from "components/turn-grid";

const font = Open_Sans({ subsets: ["latin"] });

export async function getStaticProps(context) {
  const commit = require("child_process")
    .execSync('git log -n1 --format="%h %ai"')
    .toString()
    .trim();

  const [hash, date] = commit.split(" ");
  const [YYYY, MM, DD] = date.split("-");

  return {
    props: {
      year: YYYY,
      draftId: `prototype_draft_${hash}_${[YYYY.slice(-2), MM, DD].join("")}`,
    },
  };
}

function Home({ count, year, draftId, reverse }) {
  const router = useRouter();
  count = parseInt(router.query.count) || count;
  reverse = !isUndefined(router.query.reverse) || reverse;

  const turnsData = [
    makeTurnData({
      money: [10],
      A: [0],
      B: [0],
      C: [0],
      D: [0],
      AB: [0],
      ABC: [0],
      ABCD: [0],
      opportunity: [12],
    }),
    makeTurnData({ opportunity: [12] }),
    makeTurnData({ opportunity: [8] }),
    makeTurnData({ opportunity: [8] }),
    makeTurnData({ opportunity: [4] }),
    makeTurnData({ opportunity: [4] }),
  ];

  let sheetContents = [
    <>
      <Page>
        <Rules />
      </Page>
    </>,
    <>
      {[...Array(count || 0)].map((e, i) => (
        <Page split key={i}>
          <Sidebar turnsData={turnsData} year={year} draftId={draftId} />
          <TurnGrid turnsData={turnsData} />
        </Page>
      ))}
    </>,
    <>
      {[...Array(count || 0)].map((e, i) => (
        <Page key={i}>
          <RollGrid columns={35} rows={25} />
        </Page>
      ))}
    </>,
  ];

  if (reverse) {
    sheetContents = sheetContents.reverse();
  }

  return (
    <>
      <Head>
        <title>Hardware Hustle</title>
        {/* <meta name="description" content="Generated by create next app" /> */}
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={`${font.className}`}>
        {sheetContents.map((sheet, i) => (
          <Sheet key={i}>{sheet}</Sheet>
        ))}
      </main>
    </>
  );
}

Home.defaultProps = {
  // TODO: gamesPerSheet, gameSheetCount
  count: 1,
  year: undefined,
  draftId: undefined,
  reverse: false,
};

export default Home;
